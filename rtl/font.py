def get_font_data():
    def _str_to_byte(line):
        o = 0
        for i, v in enumerate(itertools.batched(line, 2)):
            v = 1 if v == ("#", "#") else 0
            o |= v << (7 - i)
        return o

    return [_str_to_byte(x) for x in _font_bitmap]
